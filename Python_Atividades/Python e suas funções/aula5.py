#Tipo de dado bool (boleano)
#Ao questionar algo em um programa, só existem duas respostas possíveis:
#sim (true)ou não (false).
#Existem vários operadores para "questionar".
#Dentre eles o ==, que é um operador lógico que se o valor é igual a outro

print(10 == 10) #Sim => True (Verdadeiro)

print(10 == 11) #Não => False (Falso)
print(type(10==10)) #aqui ele retorna o valor primitivo (boleano)
print(type(10==11))#aqui ele retorna o valor primitivo (boleano)