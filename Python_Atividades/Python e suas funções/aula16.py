# if / elif ......../else
# se / se não se    /se não


entrada =  input('tem "Palmeiras, tem mundial" ou "não tem mundial"?')

if entrada == 'tem': #Se a entrada for igual a ('Tem'), será executado o print abaixo.
    print('Tem mundial')
     
elif entrada == 'não tem': #Se não se - será executado 'Não tem'
    print('Não tem mundial')

else: #se não, se nenhuma das opções acima for respondida, ele ira executar a mensagem abaixo.
    print('Você não respondeu nenhuma das perguntas acima.') 
            
